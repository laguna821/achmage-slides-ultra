// Regenerates src/assets/tier3Backgrounds.generated.ts — a base64 data-URI map
// of the per-theme default Tier 3 background images. Drop generated 1920×1080
// images into assets/tier3-bg/<themeId>.<ext> and run:
//   node scripts/generate-tier3-backgrounds.mjs   (or: npm run generate:tier3-bg)
//
// Same approach as scripts/generate-fonts.mjs: we inline as data URIs in a
// generated .ts (not via an esbuild loader) so the bundle is self-contained and
// the backgrounds also survive HTML export (standalone, no app:// dependency).
// Only themes that actually have an image file are emitted — partial coverage is
// fine, so main.js only grows as you add images.
import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = dirname(dirname(fileURLToPath(import.meta.url)));
const srcDir = join(root, "assets", "tier3-bg");
const target = join(root, "src", "assets", "tier3Backgrounds.generated.ts");

// native 1920 v5 themes eligible for a bundled background (matches
// TIER3_THEME_ROWS in settings.ts and THEME_PROFILES in themeRegistry.ts).
// Each entry: themeId → accepted source basenames (first found wins). The short
// alias lets you name the file naturally (e.g. cmds-dark.png).
const THEMES = [
  { id: "cmds-dark-native-1920-v5", names: ["cmds-dark-native-1920-v5", "cmds-dark"] },
  { id: "obsidian-cyan", names: ["obsidian-cyan"] },
  { id: "cobalt-sand", names: ["cobalt-sand"] },
  { id: "deep-navy-ice", names: ["deep-navy-ice"] },
  { id: "graphite-topaz", names: ["graphite-topaz"] },
  { id: "arctic-violet", names: ["arctic-violet"] },
  { id: "hallym-light", names: ["hallym-light"] },
];

// Preference order: webp first (smallest), then png/jpg.
const EXT_MIME = [
  ["webp", "image/webp"],
  ["png", "image/png"],
  ["jpg", "image/jpeg"],
  ["jpeg", "image/jpeg"],
];

// Warn when a single bundled image is large — it gets base64-inlined into
// main.js (×~1.33) and into every exported deck. webp ~150–250KB is ideal.
const WARN_BYTES = 400 * 1024;

const entries = [];
const warnings = [];
for (const { id, names } of THEMES) {
  let found = null;
  outer: for (const name of names) {
    for (const [ext, mime] of EXT_MIME) {
      const file = join(srcDir, `${name}.${ext}`);
      if (existsSync(file)) {
        found = { file, ext, mime };
        break outer;
      }
    }
  }
  if (!found) continue;
  const bytes = readFileSync(found.file);
  const b64 = bytes.toString("base64");
  entries.push([id, `data:${found.mime};base64,${b64}`, found.ext]);
  if (bytes.length > WARN_BYTES) {
    warnings.push(
      `  ⚠ ${id} (${found.ext}): ${(bytes.length / 1024).toFixed(0)} KB — consider re-exporting as webp ≤250KB`
    );
  }
}

const body = entries
  .map(([id, uri]) => `  ${JSON.stringify(id)}: ${JSON.stringify(uri)},`)
  .join("\n");

const out = `// AUTO-GENERATED by scripts/generate-tier3-backgrounds.mjs — DO NOT EDIT BY HAND.
// Per-theme default Tier 3 background images inlined as base64 data URIs. Source
// images live in assets/tier3-bg/<themeId>.<ext>. Regenerate with:
//   npm run generate:tier3-bg
// Empty when no source images are present yet (Tier 3 then degrades to no-op for
// themes without a default or a user override).
export const TIER3_DEFAULT_BACKGROUNDS: Record<string, string> = {
${body}
};
`;

writeFileSync(target, out);
if (entries.length === 0) {
  console.log(
    `Wrote ${target}\n  (no images found in ${srcDir} — drop <themeId>.webp files there)`
  );
} else {
  let totalB64 = 0;
  console.log(`Wrote ${target}`);
  for (const [id, uri, ext] of entries) {
    totalB64 += uri.length;
    console.log(`  ${id} (${ext}): ${(uri.length / 1024).toFixed(0)} KB base64`);
  }
  console.log(
    `  total inlined: ${(totalB64 / 1024 / 1024).toFixed(2)} MB (added to main.js)`
  );
  if (warnings.length) {
    console.log("\nLarge-image warnings:");
    for (const w of warnings) console.log(w);
  }
}
